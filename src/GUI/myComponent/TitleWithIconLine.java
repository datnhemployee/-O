    private void design() {
        this.setPreferredSize(
                new Dimension(
                        Line.getIconWidth()
                        + Icon.getIconWidth(),
                        Icon.getIconHeight()
                ));
        this.setOpaque(false);
        this.setBackground(new Color(0, 0, 0, 0));

        /*
        * lblIcon
         */
        lblIcon.setIcon(Icon);
        lblIcon.setLocation(new Point(0, 0));
        lblIcon.setSize(
                Icon.getIconWidth(),
                Icon.getIconHeight());
        lblIcon.setBackground(new Color(00, 0, 0, 0));

        /*
        * lblLine
         */
        lblLine.setIcon(Line);
        lblLine.setLocation(
                new Point(
                        Icon.getIconWidth(),
                        Icon.getIconHeight() / 2
                        - Line.getIconHeight()
                ));
        lblLine.setSize(
                Line.getIconWidth(),
                Line.getIconHeight());
        lblLine.setBackground(new Color(00, 0, 0, 0));

        /*
        * lblTitle
         */
        lblTitle.setText(Text);
        lblTitle.setFont(TextFont);
        lblTitle.setForeground(ForeColor);
        lblTitle.setLocation(
                new Point(
                        Icon.getIconWidth(),
                        Icon.getIconHeight() / 2
                ));
        lblTitle.setSize(
                Line.getIconWidth(),
                Icon.getIconHeight() / 2
                - Line.getIconHeight()
        );
        lblTitle.setBackground(new Color(00, 0, 0, 0));
    }

