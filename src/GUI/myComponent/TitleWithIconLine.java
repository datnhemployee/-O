    public void setTransparent(boolean chk) {
        if (chk) {
            lblIcon.setIcon(null);
            lblLine.setIcon(null);
            lblTitle.setForeground(ForeColor);
        } else {
            lblIcon.setIcon(Icon);
            lblLine.setIcon(Line);
            lblTitle.setForeground(new Color(0, 0, 0, 0));
        }
        lblIcon.repaint();
        lblLine.repaint();
        lblTitle.repaint();
        this.repaint();
    }

